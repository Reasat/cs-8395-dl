class conv_bn_3x3(nn.Module):
    def __init__(self, in_ch, out_ch, padding):
        super().__init__()
        self.block = nn.Sequential(
            nn.Conv2d(in_ch, out_ch, 3, padding=padding),
            nn.ReLU(),
            nn.BatchNorm2d(out_ch)
        )

    def forward(self, x):
        return self.block(x)
